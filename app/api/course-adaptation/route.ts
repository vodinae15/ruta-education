import { NextRequest, NextResponse } from "next/server"
import { createClient } from "@/lib/supabase/server"

interface AdaptationRequest {
  courseId: string
  studentType: 'visual' | 'auditory' | 'kinesthetic'
  content: string
  contentType: 'lesson' | 'block' | 'element'
}

interface AdaptationResponse {
  success: boolean
  adaptedContent?: string
  error?: string
  progress?: number
  qualityCheck?: {
    isCoherent: boolean
    coverageScore: number
    issues: string[]
  }
}

export async function POST(request: NextRequest) {
  try {
    console.log("🚀 [API] POST /api/course-adaptation - начало обработки")
    
    const body: AdaptationRequest = await request.json()
    const { courseId, studentType, content, contentType } = body

    if (!courseId || !studentType || !content) {
      return NextResponse.json({
        success: false,
        error: "Отсутствуют обязательные параметры"
      }, { status: 400 })
    }

    // Создаем Supabase клиент
    const supabase = await createClient()
    console.log("🔧 [API] Service client создан")

    // Получаем информацию о курсе (для тестирования используем заглушку)
    let courseTitle = "Тестовый курс"
    try {
      const { data: course, error: courseError } = await supabase
        .from("courses")
        .select("title, description, author_type")
        .eq("id", courseId)
        .single()

      if (course && !courseError) {
        courseTitle = course.title || "Тестовый курс"
      }
    } catch (error) {
      console.log("Используем заглушку для названия курса")
    }

    console.log(`📚 [API] Адаптируем контент для типа: ${studentType}`)

    // Создаем промпт для адаптации
    const adaptationPrompt = createAdaptationPrompt(content, studentType, contentType, courseTitle)
    
    // Вызываем OpenRouter API для адаптации
    const adaptedContent = await adaptContentWithAI(adaptationPrompt)
    
    if (!adaptedContent) {
      return NextResponse.json({
        success: false,
        error: "Ошибка при адаптации контента"
      }, { status: 500 })
    }

    // Проверяем качество адаптации
    const qualityCheck = validateAdaptationQuality(content, adaptedContent, studentType)
    
    if (!qualityCheck.isCoherent) {
      console.log("⚠️ [API] Обнаружены проблемы с качеством адаптации:", qualityCheck.issues)
    }

    console.log("✅ [API] Контент успешно адаптирован")
    console.log(`📊 [API] Покрытие контента: ${qualityCheck.coverageScore}%`)

    return NextResponse.json({
      success: true,
      adaptedContent,
      qualityCheck: {
        isCoherent: qualityCheck.isCoherent,
        coverageScore: qualityCheck.coverageScore,
        issues: qualityCheck.issues
      },
      progress: 100
    })

  } catch (error) {
    console.error("❌ [API] Ошибка адаптации:", error)
    return NextResponse.json({
      success: false,
      error: "Внутренняя ошибка сервера"
    }, { status: 500 })
  }
}

