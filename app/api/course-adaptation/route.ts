function getStudentTypeDescription(studentType: string): string {
  switch (studentType) {
    case 'visual': return 'визуального типа ученика (любит схемы, диаграммы, структурированную информацию)'
    case 'auditory': return 'аудиального типа ученика (любит истории, диалоги, эмоциональные примеры)'
    case 'kinesthetic': return 'кинестетического типа ученика (любит практику, действия, интерактив)'
    default: return 'общего типа ученика'
  }
}

function validateAdaptationQuality(originalContent: string, adaptedContent: string, studentType: string): {
  isCoherent: boolean;
  coverageScore: number;
  issues: string[];
} {
  const issues: string[] = []
  let coverageScore = 0
  let isCoherent = true

  // Извлекаем ключевые элементы из авторского контента
  const originalElements = extractAuthorContentElements(originalContent)
  const adaptedElements = extractAdaptedContentElements(adaptedContent)
  
  // Проверяем покрытие ключевых элементов авторского контента
  const coveredElements = originalElements.filter(element => 
    adaptedElements.some(adaptedElement => 
      adaptedElement.toLowerCase().includes(element.toLowerCase()) ||
      element.toLowerCase().includes(adaptedElement.toLowerCase())
    )
  )
  
  coverageScore = Math.round((coveredElements.length / originalElements.length) * 100)
  
  if (coverageScore < 90) {
    issues.push(`Низкое покрытие авторского контента: ${coverageScore}% (должно быть ≥90%)`)
    isCoherent = false
  }

  // Проверяем структуру 5-блочного урока
  const blockHeaders = adaptedContent.match(/БЛОК \d+:/gm) || []
  if (blockHeaders.length !== 5) {
    issues.push(`Неправильная структура урока: ${blockHeaders.length} блоков (должно быть 5)`)
    isCoherent = false
  }

  // Проверяем наличие связок между блоками (без системных символов)
  const hasConnectors = adaptedContent.includes('Изучив') || adaptedContent.includes('Теперь') || 
                       adaptedContent.includes('Освоив') || adaptedContent.includes('Объединим')
  if (!hasConnectors) {
    issues.push('Отсутствуют связки между блоками')
    isCoherent = false
  }

  // Проверяем соответствие типу восприятия
  switch (studentType) {
    case 'visual':
      const visualElements = ['схема', 'диаграмма', '→', '┌', '│', '└', 'таблица', 'структура']
      if (!visualElements.some(element => adaptedContent.toLowerCase().includes(element))) {
        issues.push('Отсутствуют визуальные элементы (схемы, диаграммы, таблицы)')
      }
      break
      
    case 'auditory':
      const auditoryElements = ['сказал', 'диалог', 'история', '"', 'рассказ', 'обсуждение']
      if (!auditoryElements.some(element => adaptedContent.toLowerCase().includes(element))) {
        issues.push('Отсутствуют аудиальные элементы (диалоги, истории, рассказы)')
      }
      break
      
    case 'kinesthetic':
      const kinestheticElements = ['задание', 'практика', 'сделайте', 'миссия', 'проект', 'действие']
      if (!kinestheticElements.some(element => adaptedContent.toLowerCase().includes(element))) {
        issues.push('Отсутствуют практические элементы (задания, проекты, действия)')
      }
      break
  }

  // Проверяем, что адаптация не расширяет тему за пределы авторского контента
  const hasExternalContent = checkForExternalContent(originalContent, adaptedContent)
  if (hasExternalContent.length > 0) {
    issues.push(`Обнаружено расширение темы: ${hasExternalContent.join(', ')}`)
    isCoherent = false
  }

  return { isCoherent, coverageScore, issues }
}

function extractAuthorContentElements(content: string): string[] {
  const elements: string[] = []
  
  // Извлекаем даты (4 цифры)
  const dates = content.match(/\b\d{4}\b/g) || []
  elements.push(...dates)
  
  // Извлекаем имена собственные (заглавные буквы)
  const names = content.match(/\b[А-ЯЁ][а-яё]+\b/g) || []
  elements.push(...names)
  
  // Извлекаем ключевые термины (выделенные жирным или в кавычках)
  const boldTerms = content.match(/\*\*([^*]+)\*\*/g)?.map(term => term.replace(/\*\*/g, '')) || []
  const quotedTerms = content.match(/"([^"]+)"/g)?.map(term => term.replace(/"/g, '')) || []
  elements.push(...boldTerms, ...quotedTerms)
  
  // Извлекаем технические термины и понятия
  const technicalTerms = content.match(/\b[а-яё]+(?:ия|ция|ость|ение|ание|ение)\b/gi) || []
  elements.push(...technicalTerms)
  
  // Извлекаем числа и проценты
  const numbers = content.match(/\b\d+(?:\.\d+)?%?\b/g) || []
  elements.push(...numbers)
  
  // Извлекаем названия организаций, компаний, продуктов
  const organizations = content.match(/\b[А-ЯЁ][а-яё]*(?:\s+[А-ЯЁ][а-яё]*)*\s+(?:ООО|ЗАО|ОАО|ИП|компания|корпорация|фирма|организация)\b/gi) || []
  elements.push(...organizations)
  
  // Убираем дубликаты и фильтруем по длине
  return [...new Set(elements)].filter(term => term.length > 2)
}

function extractAdaptedContentElements(content: string): string[] {
  // Используем ту же логику, что и для авторского контента
  return extractAuthorContentElements(content)
}

function checkForExternalContent(originalContent: string, adaptedContent: string): string[] {
  const externalElements: string[] = []
  
  // Извлекаем все уникальные слова из адаптированного контента
  const adaptedWords = adaptedContent.toLowerCase().match(/\b[а-яё]+\b/g) || []
  const originalWords = originalContent.toLowerCase().match(/\b[а-яё]+\b/g) || []
  
  // Создаем множества для быстрого поиска
  const originalWordSet = new Set(originalWords)
  
  // Ищем слова, которые есть в адаптированном контенте, но нет в оригинальном
  const uniqueAdaptedWords = [...new Set(adaptedWords)]
  const externalWords = uniqueAdaptedWords.filter(word => 
    word.length > 3 && 
    !originalWordSet.has(word) &&
    !isCommonWord(word) // Исключаем общеупотребительные слова
  )
  
  // Проверяем на наличие внешних концепций
  const externalConcepts = [
    'дополнительно', 'также важно', 'кроме того', 'следует отметить',
    'важно знать', 'необходимо помнить', 'стоит упомянуть'
  ]
  
  externalConcepts.forEach(concept => {
    if (adaptedContent.toLowerCase().includes(concept)) {
      externalElements.push(concept)
    }
  })
  
  // Если слишком много внешних слов, добавляем предупреждение
  if (externalWords.length > 10) {
    externalElements.push(`слишком много внешних терминов (${externalWords.length})`)
  }
  
  return externalElements
}

function isCommonWord(word: string): boolean {
  const commonWords = [
    'это', 'что', 'как', 'для', 'или', 'но', 'если', 'когда', 'где', 'почему',
    'который', 'которая', 'которое', 'которые', 'чтобы', 'чтобы', 'потому',
    'поэтому', 'однако', 'таким', 'образом', 'другими', 'словами', 'например',
    'также', 'еще', 'уже', 'все', 'всего', 'всех', 'всем', 'всеми', 'всему',
    'всего', 'всей', 'всю', 'всею', 'всех', 'всем', 'всеми', 'всему'
  ]
  return commonWords.includes(word.toLowerCase())
}

async function adaptContentWithAI(prompt: string): Promise<string | null> {
  // Если нет API ключа, возвращаем тестовый контент
  if (!process.env.OPENROUTER_API_KEY) {
    console.log('OpenRouter API key not found, using test content')
    return generateTestAdaptedContent(prompt)
  }

  try {
    const response = await fetch('https://openrouter.ai/api/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${process.env.OPENROUTER_API_KEY}`,
        'Content-Type': 'application/json',
        'HTTP-Referer': process.env.NEXT_PUBLIC_APP_URL || process.env.NEXT_PUBLIC_SUPABASE_URL || 'http://localhost:3000',
        'X-Title': 'Ruta Course Adaptation'
      },
      body: JSON.stringify({
        model: process.env.OPENROUTER_MODEL || 'anthropic/claude-3.5-sonnet',
        messages: [
          {
            role: 'user',
            content: prompt
          }
        ],
        max_tokens: 4000,
        temperature: 0.7
      })
    })

    if (!response.ok) {
      console.error('OpenRouter API error:', response.status, response.statusText)
      return generateTestAdaptedContent(prompt)
    }

    const data = await response.json()
    return data.choices?.[0]?.message?.content || generateTestAdaptedContent(prompt)

  } catch (error) {
    console.error('Error calling OpenRouter API:', error)
    return generateTestAdaptedContent(prompt)
  }
}

